/* USER CODE BEGIN 4 */
void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim) {
	if (htim->Instance == TIM1) { // PPS timer
		if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) {
			uint32_t period = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
			// uint32_t pulse  = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

			// Check if period has valid number
			if (period > 0) {
				pps = (F_CLK / 768.0f) / period; // 768f is timer prescaler + 1

				cal_factor = 1.0f / pps; // Calculate calibration factor. 1.0f is the frequency the PPS output is set to. Default is 1 Hz.
			}
		}
	}

    if (htim->Instance == TIM3) { // Net mains timer
        if (htim->Channel == HAL_TIM_ACTIVE_CHANNEL_2) {
            // Falling edge: CH1 (period) and CH2 (pulse) are both valid for this cycle
            uint32_t period = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_1);
            uint32_t pulse  = HAL_TIM_ReadCapturedValue(htim, TIM_CHANNEL_2);

            // Check if period has valid number and not already max number of cycles in buffer
            if (period > 0 && mesIndex < NUM_CYCLES) {
            	// Write measurement to buffer
                Duties[mesIndex]      = (pulse * 100.0f) / period;
                Frequencies[mesIndex] = (F_CLK / 6.0f) / period; // 6.0f is timer prescaler + 1

                mesIndex++; // Increment measuring index
            }
        }
    }
}
